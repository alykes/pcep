# If you supply the wrong value, you get a TypeError exception
# Demonstrating the ord() function.
char_1 = 'a'
char_2 = ' '  # space

print(ord(char_1))
print(ord(char_2))


# If you supply a negative number, you get a ValueError exception, supplying a string gives you a TypeError exception
# Demonstrating the chr() function.
print(chr(97))
print(chr(945))

