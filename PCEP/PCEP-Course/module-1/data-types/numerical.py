# You can use underscores in numbers now as a separator
# 120000300
# 12_000_300

# You can also use scientific notation
# 3e4 = 3E4 = 3 * 10000 = 30000
# 3e-4 = 3E-4 = 3 * 1/10000 = 0.0003
print(0.000000000000000000000000003)

# Octal Numbers
# These start with 0o or 0O
print(0o123)

# Hexadecimal Numbers
# These start with 0X or 0x
print (0x123)